"""
    diagram_certificate_payload(D::Diagram) -> Dict

Generate a certificate payload from a diagram for Lean proof verification.
"""
function diagram_certificate_payload(D::Diagram)
    ir = to_ir(D)
    Dict{String, Any}(
        "diagram_name" => String(ir.name),
        "objects" => [String(o.name) for o in ir.objects],
        "operations" => [
            let
                kind_str, refs = _operation_decl(op)
                Dict{String, Any}(
                    "name" => get(op, :name, ""),
                    "kind" => kind_str,
                    "refs" => refs,
                )
            end
            for op in ir.operations
        ],
        "ports" => [
            Dict{String, Any}(
                "name" => String(p.name),
                "ref" => String(p.ref),
                "kind" => String(p.kind),
                "port_type" => String(p.port_type),
                "direction" => lowercase(string(p.direction)),
            )
            for p in ir.ports
        ],
    )
end

function _operation_decl(op::Dict{Symbol, Any})
    kind = op[:kind]
    if kind == :morphism
        ("OperationKind.morphism", [String(op[:source]), String(op[:target])])
    elseif kind == :composition
        refs = String.(op[:chain])
        push!(refs, String(op[:source]))
        push!(refs, String(op[:target]))
        ("OperationKind.composition", refs)
    elseif kind == :kanextension
        dir = op[:direction]
        kind_str = dir == :left ? "OperationKind.leftKan" : "OperationKind.rightKan"
        refs = [String(op[:source]), String(op[:along])]
        op[:target] !== nothing && push!(refs, String(op[:target]))
        (kind_str, refs)
    else
        ("OperationKind.unknown", String[])
    end
end

"""
    render_lean_certificate(D::Diagram; module_name=nothing) -> String

Generate Lean 4 source code for a diagram certificate.
"""
function render_lean_certificate(D::Diagram; module_name::Union{Nothing, String}=nothing)
    mod = module_name === nothing ? _sanitize_module_name(String(D.name)) : module_name
    payload = diagram_certificate_payload(D)

    lines = String[]
    push!(lines, "-- Auto-generated by FunctorFlow.jl")
    push!(lines, "namespace FunctorFlowProofs.Generated.$mod")
    push!(lines, "")
    push!(lines, "open FunctorFlowProofs in")
    push!(lines, "")

    # DiagramDecl
    push!(lines, "def exportedDiagram : DiagramDecl := {")
    push!(lines, "  name := $(lean_string(payload["diagram_name"])),")
    push!(lines, "  objects := $(lean_list([lean_string(o) for o in payload["objects"]])),")

    op_strs = String[]
    for op in payload["operations"]
        refs_str = lean_list([lean_string(r) for r in op["refs"]])
        push!(op_strs, "{ name := $(lean_string(op["name"])), kind := $(op["kind"]), refs := $refs_str }")
    end
    push!(lines, "  operations := $(lean_list(op_strs)),")

    port_strs = String[]
    for p in payload["ports"]
        push!(port_strs, "{ name := $(lean_string(p["name"])), ref := $(lean_string(p["ref"])), kind := $(lean_string(p["kind"])), portType := $(lean_string(p["port_type"])), direction := $(lean_string(p["direction"])) }")
    end
    push!(lines, "  ports := $(lean_list(port_strs))")
    push!(lines, "}")
    push!(lines, "")

    # Lowering artifact
    push!(lines, "def exportedArtifact : LoweringArtifact := {")
    push!(lines, "  diagram := exportedDiagram,")
    push!(lines, "  resolvedRefs := true,")
    push!(lines, "  portsClosed := true")
    push!(lines, "}")
    push!(lines, "")

    # Theorems
    push!(lines, "theorem exportedArtifact_checks : exportedArtifact.check = true := by native_decide")
    push!(lines, "")
    push!(lines, "theorem exportedArtifact_sound : exportedArtifact.Sound :=")
    push!(lines, "  LoweringArtifact.sound_of_check exportedArtifact_checks")
    push!(lines, "")
    push!(lines, "end FunctorFlowProofs.Generated.$mod")

    join(lines, "\n")
end

"""
    render_construction_certificate(uc::UniversalConstruction; module_name=nothing) -> String

Generate a Lean 4 certificate that declares and verifies a universal construction.
Includes the construction type, participating diagrams, and structural theorems.
"""
function render_construction_certificate(uc::PullbackResult; module_name::Union{Nothing, String}=nothing)
    mod = module_name === nothing ? _sanitize_module_name(String(uc.name)) : module_name
    base_cert = render_lean_certificate(uc.cone; module_name=mod)

    # Insert construction declaration before the closing namespace
    construction_lines = String[]
    push!(construction_lines, "")
    push!(construction_lines, "-- Pullback construction declaration")
    push!(construction_lines, "def pullbackDecl : ConstructionDecl := {")
    push!(construction_lines, "  kind := ConstructionKind.pullback,")
    push!(construction_lines, "  diagram := exportedDiagram,")
    push!(construction_lines, "  projection1 := $(lean_string(String(uc.projection1))),")
    push!(construction_lines, "  projection2 := $(lean_string(String(uc.projection2))),")
    push!(construction_lines, "  sharedObject := $(lean_string(String(uc.shared_object))),")
    push!(construction_lines, "  interfaceMorphisms := $(lean_list([lean_string(String(m)) for m in uc.interface_morphisms]))")
    push!(construction_lines, "}")
    push!(construction_lines, "")
    push!(construction_lines, "-- Commuting square theorem: both projections agree on shared interface")
    push!(construction_lines, "theorem pullback_commutes : pullbackDecl.CommutingSquare := by")
    push!(construction_lines, "  exact ConstructionDecl.commuting_of_loss exportedArtifact")
    push!(construction_lines, "")

    # Insert before the end namespace line
    lines = split(base_cert, "\n")
    insert_idx = findlast(l -> startswith(l, "end "), lines)
    if insert_idx !== nothing
        result_lines = vcat(lines[1:insert_idx-1], construction_lines, [lines[insert_idx]])
        join(result_lines, "\n")
    else
        base_cert * "\n" * join(construction_lines, "\n")
    end
end

