"""
    render_jepa_certificate(D::Diagram; module_name=nothing) -> String

Generate a Lean 4 certificate for a JEPA diagram, including:
- Standard diagram certificate
- Coalgebra declarations
- JEPA prediction-as-obstruction theorems
- Bisimulation declarations (if present)
"""
function render_jepa_certificate(D::Diagram; module_name::Union{Nothing, String}=nothing)
    mod = module_name === nothing ? _sanitize_module_name(String(D.name)) : module_name
    base_cert = render_lean_certificate(D; module_name=mod)

    coalgebras = get_coalgebras(D)
    bisimulations = get_bisimulations(D)

    extra_lines = String[]

    # Coalgebra declarations
    if !isempty(coalgebras)
        push!(extra_lines, "")
        push!(extra_lines, "-- Coalgebra declarations")
        for (name, c) in coalgebras
            push!(extra_lines, "def coalgebra_$(name) : CoalgebraDecl := {")
            push!(extra_lines, "  name := $(lean_string(String(name))),")
            push!(extra_lines, "  state := $(lean_string(String(c.state))),")
            push!(extra_lines, "  transition := $(lean_string(String(c.transition))),")
            push!(extra_lines, "  functorType := $(lean_string(String(c.functor_type)))")
            push!(extra_lines, "}")
            push!(extra_lines, "")
        end
    end

    # JEPA-specific theorems
    jepa_losses = [l for l in values(D.losses)
                   if get(l.metadata, :macro, nothing) in (:JEPA, :KanJEPA)]
    if !isempty(jepa_losses)
        push!(extra_lines, "-- JEPA prediction-as-obstruction theorems")
        push!(extra_lines, "/-- The JEPA prediction loss is an obstruction to commutativity")
        push!(extra_lines, "    of the encoder/predictor square. When loss = 0, the square")
        push!(extra_lines, "    commutes and the encoder is an exact coalgebra morphism. -/")
        for loss in jepa_losses
            lname = String(loss.name)
            push!(extra_lines, "theorem jepa_$(lname)_is_obstruction :")
            push!(extra_lines, "    exportedArtifact.lossIsObstruction $(lean_string(lname)) := by")
            push!(extra_lines, "  exact LoweringArtifact.loss_obstruction_of_check exportedArtifact_checks")
            push!(extra_lines, "")
        end

        push!(extra_lines, "/-- When the JEPA prediction loss is zero, the encoder-predictor")
        push!(extra_lines, "    path commutes with the target encoder path, making the")
        push!(extra_lines, "    encoder a coalgebra morphism (structure-preserving map). -/")
        push!(extra_lines, "theorem jepa_exact_implies_coalgebra_morphism")
        push!(extra_lines, "    (h : ∀ l ∈ exportedArtifact.losses, l.value = 0) :")
        push!(extra_lines, "    exportedArtifact.CoalgebraExact := by")
        push!(extra_lines, "  exact LoweringArtifact.coalgebra_exact_of_zero_loss h")
        push!(extra_lines, "")
    end

    # Bisimulation declarations
    if !isempty(bisimulations)
        push!(extra_lines, "-- Bisimulation declarations")
        for (name, b) in bisimulations
            push!(extra_lines, "def bisim_$(name) : BisimulationDecl := {")
            push!(extra_lines, "  name := $(lean_string(String(name))),")
            push!(extra_lines, "  coalgebraA := $(lean_string(String(b.coalgebra_a))),")
            push!(extra_lines, "  coalgebraB := $(lean_string(String(b.coalgebra_b))),")
            push!(extra_lines, "  relation := $(lean_string(String(b.relation)))")
            push!(extra_lines, "}")
            push!(extra_lines, "")
        end

        push!(extra_lines, "/-- Two coalgebras are bisimilar iff they map to the same element")
        push!(extra_lines, "    in the final coalgebra — behavioral equivalence. -/")
        push!(extra_lines, "theorem bisimilar_iff_final_coalgebra_equal")
        push!(extra_lines, "    (A B : CoalgebraDecl) (R : BisimulationDecl)")
        push!(extra_lines, "    (h : R.isBisimulation A B) :")
        push!(extra_lines, "    A.finalImage = B.finalImage :=")
        push!(extra_lines, "  CoalgebraDecl.bisim_implies_final_eq h")
        push!(extra_lines, "")
    end

    # Energy function declarations
    energy_fns = get_energy_functions(D)
    if !isempty(energy_fns)
        push!(extra_lines, "-- Energy function declarations")
        for (name, ef) in energy_fns
            push!(extra_lines, "def energy_$(name) : EnergyDecl := {")
            push!(extra_lines, "  name := $(lean_string(String(name))),")
            push!(extra_lines, "  domain := $(lean_list([lean_string(String(d)) for d in ef.domain])),")
            push!(extra_lines, "  energyType := $(lean_string(String(ef.energy_type)))")
            push!(extra_lines, "}")
            push!(extra_lines, "")
        end

        push!(extra_lines, "/-- Energy functions are non-negative for L2 and cosine types. -/")
        push!(extra_lines, "theorem energy_nonneg (e : EnergyDecl)")
        push!(extra_lines, "    (h : e.energyType ∈ [\"l2\", \"cosine\"]) :")
        push!(extra_lines, "    0 ≤ e.evaluate := by")
        push!(extra_lines, "  exact EnergyDecl.nonneg_of_standard h")
        push!(extra_lines, "")
    end

    # Insert before end namespace
    if !isempty(extra_lines)
        lines = split(base_cert, "\n")
        insert_idx = findlast(l -> startswith(l, "end "), lines)
        if insert_idx !== nothing
            result_lines = vcat(lines[1:insert_idx-1], extra_lines, [lines[insert_idx]])
            return join(result_lines, "\n")
        end
    end

    base_cert
end
