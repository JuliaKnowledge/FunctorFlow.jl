"""
    write_lean_certificate(D::Diagram; module_name=nothing, output_dir=".") -> String

Write a Lean certificate file for the diagram. Returns the file path.
"""
function write_lean_certificate(D::Diagram;
                                module_name::Union{Nothing, String}=nothing,
                                output_dir::AbstractString=".")
    mod = module_name === nothing ? _sanitize_module_name(String(D.name)) : module_name
    content = render_lean_certificate(D; module_name=mod)
    path = joinpath(output_dir, "$mod.lean")
    mkpath(dirname(path))
    write(path, content)
    path
end

function _sanitize_module_name(name::String)
    parts = split(replace(name, r"[^a-zA-Z0-9_]" => "_"), "_"; keepempty=false)
    join(uppercasefirst.(parts))
end

lean_string(s::AbstractString) = "\"$(escape_string(s))\""
lean_string(s::Symbol) = lean_string(String(s))
lean_list(items::Vector) = isempty(items) ? "[]" : "[$(join(items, ", "))]"

